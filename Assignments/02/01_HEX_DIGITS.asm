    MOV AL, CHX
    OUT DX, AL
    
    MOV AL, DHX
    OUT DX, AL
    
    MOV AL, EHX
    OUT DX, AL
    
    MOV AL, FHX
    OUT DX, AL
    
    LOOP PRINT

    EXIT:
